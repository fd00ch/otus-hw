package ru.otus.fortesting;

public class Calculator {
    public int multiply(int a, int b) {
        return a * b;
    }
    public int division(int a, int b) {
        return a / b;
    }
    public int sum(int a, int b) {
        return a + b;
    }
    public int subtraction(int a, int b) {
        return a - b;
    }
}
